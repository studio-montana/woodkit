/**
 * register JS
*/
function iconpicker_ajax_admin_enqueue_scripts(){
	$iconpicker_ajax_js_file = locate_web_ressource(WOODKIT_PLUGIN_COMMONS_FOLDER.'iconpicker/iconpicker.js');
	if (!empty($iconpicker_ajax_js_file)){
		wp_enqueue_script('iconpicker-ajax', $iconpicker_ajax_js_file, array('jquery'), "1.0");
		wp_localize_script('iconpicker-ajax', 'Iconpicker', array(
		'ajaxUrl' => admin_url('admin-ajax.php'),
		'ajaxNonce' => wp_create_nonce('iconpicker-ajax-nonce'),
		'doneButtonText' => __("Close", WOODKIT_PLUGIN_TEXT_DOMAIN),
		)
		);
	}
}
add_action('admin_enqueue_scripts', 'iconpicker_ajax_admin_enqueue_scripts');

/**
 * retrieve posts
*/
function iconpicker_ajax_iconpicker_get_icons() {
	if (!check_ajax_referer('iconpicker-ajax-nonce', 'ajaxNonce', false)){
		die ('Busted!');
	}

	$response = array('what'=>'iconpicker_ajax_iconpicker_get_icons',
			'action'=>'iconpicker_ajax_iconpicker_get_icons',
			'id'=>'1'
	);

	ob_start();
	$iconpick_items_template = locate_ressource(WOODKIT_PLUGIN_COMMONS_FOLDER.'/iconpicker/templates/iconpicker-items.php');
	if (!empty($iconpick_items_template))
		include($iconpick_items_template);
	$results = ob_get_contents();
	ob_end_clean();

	$response['data'] = $results;

	$xmlResponse = new WP_Ajax_Response($response);
	$xmlResponse->send();
	exit();
}
add_action('wp_ajax_iconpicker_get_icons', 'iconpicker_ajax_iconpicker_get_icons');

